from __future__ import annotations

import argparse
import csv
import json
import sqlite3
import sys
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Callable, TypeVar

PROJECT_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PROJECT_ROOT))

from src.api_client import SportsPredictAPIError, SportsPredictClient
from src.config import load_settings


DEFAULT_PREDICTIONS_PATH = Path("reports/dry_run_predictions.csv")
DEFAULT_REPORTS_DIR = Path("reports")
BATCH_SIZE = 50
DEFAULT_UPDATE_DELAY_SECONDS = 0.35
DEFAULT_RETRY_SECONDS = 8.0
DEFAULT_MAX_RETRIES = 5
T = TypeVar("T")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Submit Version 1 predictions to the SportsPredict Probability Cup API."
    )
    parser.add_argument(
        "--predictions",
        type=Path,
        default=DEFAULT_PREDICTIONS_PATH,
        help="Prediction CSV generated by scripts/04_predict_markets.py.",
    )
    parser.add_argument(
        "--confirm",
        action="store_true",
        help="Actually submit/update predictions. Without this flag, only prints a dry run.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional max number of predictions to submit/update, useful for a tiny live smoke test.",
    )
    parser.add_argument(
        "--report-dir",
        type=Path,
        default=DEFAULT_REPORTS_DIR,
        help="Where to write submission response JSON.",
    )
    parser.add_argument(
        "--update-delay-seconds",
        type=float,
        default=DEFAULT_UPDATE_DELAY_SECONDS,
        help="Delay between individual PATCH updates to avoid API throttling.",
    )
    parser.add_argument(
        "--retry-seconds",
        type=float,
        default=DEFAULT_RETRY_SECONDS,
        help="Initial wait after a 429 rate-limit response. Retries use linear backoff.",
    )
    parser.add_argument(
        "--max-retries",
        type=int,
        default=DEFAULT_MAX_RETRIES,
        help="Maximum retries for each API request that hits a 429 rate limit.",
    )
    return parser.parse_args()


def utc_stamp() -> str:
    return datetime.now(timezone.utc).isoformat().replace(":", "-")


def probability_to_api_int(probability: str) -> int:
    value = float(probability)
    if value <= 1.0:
        value *= 100.0
    return max(1, min(99, round(value)))


def parse_existing_probability(prediction: dict[str, Any]) -> int | None:
    for key in ("probability", "probability_percent", "probabilityPercent"):
        if key not in prediction or prediction[key] is None:
            continue
        try:
            return probability_to_api_int(str(prediction[key]))
        except (TypeError, ValueError):
            continue
    return None


def load_prediction_rows(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as input_file:
        rows = list(csv.DictReader(input_file))
    required = {"market_id", "probability", "match_name", "question"}
    missing = required - set(rows[0].keys() if rows else [])
    if missing:
        raise ValueError(f"Prediction CSV missing columns: {sorted(missing)}")
    return rows


def current_open_market_lobbies(database_path: Path) -> dict[str, str]:
    now_iso = datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")
    with sqlite3.connect(database_path) as connection:
        return {
            market_id: lobby_id
            for market_id, lobby_id in connection.execute(
                """
                SELECT id, lobby_id
                FROM markets
                WHERE status = 'open'
                  AND (closing_time IS NULL OR closing_time > ?)
                """,
                (now_iso,),
            ).fetchall()
        }


def build_payloads(
    prediction_rows: list[dict[str, str]],
    market_lobbies: dict[str, str],
) -> list[dict[str, Any]]:
    payloads: list[dict[str, Any]] = []
    for row in prediction_rows:
        market_id = row["market_id"]
        lobby_id = market_lobbies.get(market_id)
        if not lobby_id:
            continue
        payloads.append(
            {
                "market_id": market_id,
                "lobby_id": lobby_id,
                "probability": probability_to_api_int(row["probability"]),
            }
        )
    return payloads


def chunks(items: list[dict[str, Any]], size: int) -> list[list[dict[str, Any]]]:
    return [items[index : index + size] for index in range(0, len(items), size)]


def existing_predictions_by_market(
    client: SportsPredictClient,
    lobby_ids: set[str],
) -> dict[str, dict[str, Any]]:
    existing: dict[str, dict[str, Any]] = {}
    for lobby_id in lobby_ids:
        for prediction in client.get_predictions(lobby_id=lobby_id):
            market_id = prediction.get("market_id")
            if market_id:
                existing[market_id] = prediction
    return existing


def is_rate_limit_error(error: SportsPredictAPIError) -> bool:
    return "429" in str(error) or "Too Many Requests" in str(error)


def call_with_rate_limit_retries(
    operation: Callable[[], T],
    label: str,
    max_retries: int,
    retry_seconds: float,
) -> T:
    attempt = 0
    while True:
        try:
            return operation()
        except SportsPredictAPIError as exc:
            attempt += 1
            if not is_rate_limit_error(exc) or attempt > max_retries:
                raise
            wait_seconds = retry_seconds * attempt
            print(
                f"Rate limited during {label}. "
                f"Waiting {wait_seconds:.1f}s before retry {attempt}/{max_retries}..."
            )
            time.sleep(wait_seconds)


def write_report(report_dir: Path, payload: dict[str, Any]) -> Path:
    report_dir.mkdir(parents=True, exist_ok=True)
    path = report_dir / f"{utc_stamp()}_submission_response.json"
    path.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")
    return path


def main() -> int:
    args = parse_args()
    settings = load_settings()

    if not settings.has_api_key:
        print("Missing SPORTSPREDICT_API_KEY in .env")
        return 1
    if not args.predictions.exists():
        print(f"Prediction CSV not found: {args.predictions}")
        print("Run scripts/04_predict_markets.py first.")
        return 1
    if not settings.database_path.exists():
        print(f"Database not found: {settings.database_path}")
        print("Run scripts/01_fetch_sportspredict.py first.")
        return 1

    rows = load_prediction_rows(args.predictions)
    market_lobbies = current_open_market_lobbies(settings.database_path)
    payloads = build_payloads(rows, market_lobbies)
    if args.limit is not None:
        payloads = payloads[: args.limit]

    if not payloads:
        print("No open, unexpired predictions to submit.")
        return 1

    print(f"Prediction CSV rows: {len(rows)}")
    print(f"Open/unexpired payloads: {len(payloads)}")
    print(f"Mode: {'LIVE SUBMISSION' if args.confirm else 'DRY RUN'}")
    print("")
    print("Sample payloads:")
    for payload in payloads[:5]:
        print(f"- {payload}")

    if not args.confirm:
        print(f"Submit batch size: {BATCH_SIZE}")
        print("")
        print("Dry run only. Add --confirm to submit these predictions to SportsPredict.")
        return 0

    client = SportsPredictClient(
        api_key=settings.sportspredict_api_key,
        base_url=settings.sportspredict_base_url,
    )

    existing_by_market = existing_predictions_by_market(
        client,
        {str(payload["lobby_id"]) for payload in payloads},
    )
    update_payloads = [
        payload
        for payload in payloads
        if payload["market_id"] in existing_by_market
    ]
    changed_update_payloads = [
        payload
        for payload in update_payloads
        if parse_existing_probability(existing_by_market[payload["market_id"]])
        != int(payload["probability"])
    ]
    submit_payloads = [
        payload
        for payload in payloads
        if payload["market_id"] not in existing_by_market
    ]

    print(f"Existing predictions to update: {len(update_payloads)}")
    print(f"Existing predictions already unchanged: {len(update_payloads) - len(changed_update_payloads)}")
    print(f"Existing predictions changed: {len(changed_update_payloads)}")
    print(f"New predictions to submit: {len(submit_payloads)}")
    print(f"Update delay: {args.update_delay_seconds:.2f}s")
    print(f"Rate-limit retries: {args.max_retries} with {args.retry_seconds:.1f}s base wait")

    submit_responses: list[dict[str, Any]] = []
    update_responses: list[dict[str, Any]] = []
    skipped_update_payloads = len(update_payloads) - len(changed_update_payloads)
    try:
        for index, payload in enumerate(changed_update_payloads, start=1):
            prediction_id = existing_by_market[payload["market_id"]]["id"]
            update_responses.append(
                call_with_rate_limit_retries(
                    lambda prediction_id=prediction_id, probability=int(payload["probability"]): client.update_prediction(
                        prediction_id,
                        probability,
                    ),
                    f"update {index}/{len(changed_update_payloads)}",
                    args.max_retries,
                    args.retry_seconds,
                )
            )
            if args.update_delay_seconds > 0 and index < len(changed_update_payloads):
                time.sleep(args.update_delay_seconds)

        for batch_number, batch in enumerate(chunks(submit_payloads, BATCH_SIZE), start=1):
            response = call_with_rate_limit_retries(
                lambda batch=batch: client.submit_predictions_batch(batch),
                f"submit batch {batch_number}",
                args.max_retries,
                args.retry_seconds,
            )
            submit_responses.append(response)
            print(
                f"Batch {batch_number}: "
                f"{response.get('succeeded', 0)} succeeded, "
                f"{response.get('failed', 0)} failed"
            )
    except SportsPredictAPIError as exc:
        print(f"SportsPredict API error: {exc}")
        report_path = write_report(
            args.report_dir,
            {
                "mode": "submit_or_update",
                "partial_submit_responses": submit_responses,
                "partial_update_responses": update_responses,
                "skipped_updates": skipped_update_payloads,
                "error": str(exc),
            },
        )
        print(f"Partial report: {report_path}")
        return 1

    report_path = write_report(
        args.report_dir,
        {
            "mode": "submit_or_update",
            "payloads": len(payloads),
            "submitted": len(submit_payloads),
            "updated": len(changed_update_payloads),
            "skipped_updates": skipped_update_payloads,
            "submit_responses": submit_responses,
            "update_responses": update_responses,
        },
    )
    total_succeeded = len(update_responses) + sum(
        int(response.get("succeeded", 0))
        for response in submit_responses
    )
    total_failed = sum(int(response.get("failed", 0)) for response in submit_responses)
    print("")
    print(f"Submission complete: {total_succeeded} succeeded, {total_failed} failed")
    print(f"Submission report: {report_path}")
    return 0 if total_failed == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
